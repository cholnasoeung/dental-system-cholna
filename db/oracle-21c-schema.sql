CREATE TABLE PATIENTS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  FULL_NAME VARCHAR2(200) NOT NULL,
  DATE_OF_BIRTH DATE,
  GENDER VARCHAR2(20),
  PHONE VARCHAR2(30),
  EMAIL VARCHAR2(150),
  ADDRESS VARCHAR2(300),
  EMERGENCY_CONTACT_NAME VARCHAR2(200),
  EMERGENCY_CONTACT_PHONE VARCHAR2(30),
  MEDICAL_NOTES CLOB,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE STAFF (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  FULL_NAME VARCHAR2(200) NOT NULL,
  EMAIL VARCHAR2(150) NOT NULL UNIQUE,
  PASSWORD_HASH VARCHAR2(255) NOT NULL,
  ROLE VARCHAR2(50) NOT NULL,
  PHONE VARCHAR2(30),
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE APPOINTMENTS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PATIENT_ID NUMBER NOT NULL,
  STAFF_ID NUMBER,
  APPOINTMENT_DATE TIMESTAMP NOT NULL,
  STATUS VARCHAR2(50) NOT NULL,
  REASON VARCHAR2(300),
  NOTES CLOB,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_APPOINTMENTS_PATIENT
    FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS(ID),
  CONSTRAINT FK_APPOINTMENTS_STAFF
    FOREIGN KEY (STAFF_ID) REFERENCES STAFF(ID)
);

CREATE TABLE EMR_RECORDS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PATIENT_ID NUMBER NOT NULL,
  STAFF_ID NUMBER,
  DIAGNOSIS CLOB,
  TREATMENT_PLAN CLOB,
  CLINICAL_NOTES CLOB,
  RECORD_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_EMR_PATIENT
    FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS(ID),
  CONSTRAINT FK_EMR_STAFF
    FOREIGN KEY (STAFF_ID) REFERENCES STAFF(ID)
);

CREATE TABLE PRESCRIPTIONS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PATIENT_ID NUMBER NOT NULL,
  RECORD_ID NUMBER,
  MEDICATION_NAME VARCHAR2(200) NOT NULL,
  DOSAGE VARCHAR2(100),
  FREQUENCY VARCHAR2(100),
  DURATION VARCHAR2(100),
  INSTRUCTIONS CLOB,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_PRESCRIPTIONS_PATIENT
    FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS(ID),
  CONSTRAINT FK_PRESCRIPTIONS_RECORD
    FOREIGN KEY (RECORD_ID) REFERENCES EMR_RECORDS(ID)
);

CREATE TABLE INVOICES (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PATIENT_ID NUMBER NOT NULL,
  RECORD_ID NUMBER,
  TOTAL_AMOUNT NUMBER(10,2) NOT NULL,
  STATUS VARCHAR2(50) NOT NULL,
  NOTES CLOB,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_INVOICES_PATIENT
    FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS(ID),
  CONSTRAINT FK_INVOICES_RECORD
    FOREIGN KEY (RECORD_ID) REFERENCES EMR_RECORDS(ID)
);

CREATE TABLE NOTIFICATIONS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PATIENT_ID NUMBER,
  TITLE VARCHAR2(200) NOT NULL,
  MESSAGE CLOB NOT NULL,
  TYPE VARCHAR2(50),
  IS_READ NUMBER(1) DEFAULT 0,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_NOTIFICATIONS_PATIENT
    FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS(ID)
);

CREATE TABLE SUPPORT_TICKETS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PATIENT_ID NUMBER,
  SUBJECT VARCHAR2(200) NOT NULL,
  STATUS VARCHAR2(50) NOT NULL,
  PRIORITY VARCHAR2(50),
  DESCRIPTION CLOB,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_SUPPORT_PATIENT
    FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS(ID)
);

CREATE TABLE SUPPORT_MESSAGES (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TICKET_ID NUMBER NOT NULL,
  SENDER_NAME VARCHAR2(200),
  MESSAGE CLOB NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT FK_SUPPORT_MESSAGES_TICKET
    FOREIGN KEY (TICKET_ID) REFERENCES SUPPORT_TICKETS(ID)
);

CREATE TABLE ANALYTICS_EVENTS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  EVENT_NAME VARCHAR2(150) NOT NULL,
  EVENT_DATA CLOB,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
